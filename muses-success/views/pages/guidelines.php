<h1>Submission Guidelines</h1>

<h3>Acceptable Web Fiction</h3>

<ul>
<li>Both completed web novels and ongoing web serials (collectively web fiction) 
are acceptable.
    <ul>
        <li>Short story collections are also acceptable if they are related in some way 
such as by location, abilities or characters.</li>
    </ul>
</li>
<li>The submission must be available freely via a web-accessible medium.

<ul>
<li>By this we mean viewable in a web browser by over 80% of readers (ie. PDF is
    acceptable but ePub would not be).</li>
</ul></li>
<li>The submission may be available in print but must be fully available for free 
online</li>
<li>The submission must be original. Fan fiction is not acceptable.</li>
<li>The web fiction must be written in the English language or have an officially endorsed english translation.</li>
<li>Fiction hosted on websites like 
<a href="http://fictionpress.com/">FictionPress</a>, 
<a href="http://writing.com/">Writing.com</a>, <a href="http://deviantart.com">DeviantART</a> and similar web sites is not 
acceptable.</li>

<li>The web fiction must not be primarily erotica. Sex in service of the story
is fine as is fan service but sexual activity for the sake of sex is not.</li>
<li>Web fiction requiring registration to read is acceptable but registration must be free, additionally this should be noted towards the end of the synopsis.</li>
</ul>

