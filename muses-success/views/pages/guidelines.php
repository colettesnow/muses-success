<h3>Multiple Books in a Series</h3>

<p>Each book in a series should have its own listing. Titles should be given as follows: [series title] - [book title] (Book NUM). For example: An Intimate History of the Greater Kingdom - The Tale of Two Kingdoms (Book 1)</p>


<h3>Capitalisation</h3>

<p>In some cases, the official titles of web fiction are in entirely uppercase or lowercase. 
In these cases, in the title field of a listing, these should be capitalised as defined in <a href="http://wikipedia.org/">Wikipedia</a>'s <a href="http://en.wikipedia.org/wiki/Wikipedia:Manual_of_Style_%28capital_letters%29">Manual of Style</a>.</p>

<h3>Synopsis from External Sources</h3>

<p>It is possible to add a synopsis to web fiction listings. While original synopsis are preferred, using synopsises from external resources is allowed providing that the source is properly credited.</p>
<p>Sources should be credited by appending something along the following lines to the end of a synopsis:</p>

<p>[b]Source:[/b] [url=URL]TITLE[/url] which would result in: <strong>Source:</strong> <a href="url">TITLE</a>.</p>
<p>If the synopsis has been edited, this should be specified in brackets next to the source link as follows:</p>
<p><strong>Source:</strong> <a href="http://en.wikipedia.org/wiki/Blog">Wikipedia</a> (Edited)</p>

